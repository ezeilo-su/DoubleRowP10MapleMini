#ifndef DELAY_H_
#define DELAY_H_
//**********************************************************************************************
//**********************************************************************************************
void delay_ms(uint32_t delay)
{
 	while(delay)
	{
	 	msTick=0;
		while(!msTick);
		delay--;
	}
}
//**********************************************************************************************
//**********************************************************************************************
void delay_us(uint16_t t)
{
	TIM2->CNT=0;	//confirm this thence!!
	while(TIM2->CNT<t);
}


#endif /* DELAY_H_ */
